/**
	This table is the reference for a user object, this will contain the information of the user, 
	and the relation with the role that the user have
*/
CREATE TABLE t_user (
  id 		INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 1, INCREMENT BY 1) PRIMARY KEY,
  name 		VARCHAR(30),
  email  	VARCHAR(50),
  password 	VARCHAR(20),
  enable 	BOOLEAN,
  role_id	FOREIGN KEY (role_id) REFERENCES PUBLIC.t_role (role_id)
);

/**
	This is a catalog of the roles that the application have
	The role have a direct relation with the user, a user must have at least one role
*/
CREATE TABLE t_role (
	role_id INTEGER GENERATED BY DEFAULT AS IDENTITY (START WITH 1, INCREMENT BY 1) PRIMARY KEY,
	role_name VARCHAR(100),
	role_description VARCHAR(250)
);

SELECT * FROM t_role;